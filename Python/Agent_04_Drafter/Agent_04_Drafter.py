def should_continue(state: AgentState) -> str:
    """
    –ê–Ω–∞–ª–∏–∑–∏—Ä—É–µ—Ç –∏—Å—Ç–æ—Ä–∏—é —Å–æ–æ–±—â–µ–Ω–∏–π, —á—Ç–æ–±—ã –æ–ø—Ä–µ–¥–µ–ª–∏—Ç—å:
    –ø—Ä–æ–¥–æ–ª–∂–∞—Ç—å —Ä–∞–±–æ—Ç—É ("continue") –∏–ª–∏ –∑–∞–≤–µ—Ä—à–∏—Ç—å ("end").
    """

    messages = state["messages"]

    if not messages:
        return "continue"

    # –ò—â–µ–º –ø–æ—Å–ª–µ–¥–Ω–µ–µ ToolMessage, —Å–≤—è–∑–∞–Ω–Ω–æ–µ —Å —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏–µ–º –¥–æ–∫—É–º–µ–Ω—Ç–∞.
    for message in reversed(messages):
        # –ï—Å–ª–∏ —ç—Ç–æ –æ—Ç–≤–µ—Ç –∏–Ω—Å—Ç—Ä—É–º–µ–Ω—Ç–∞ –∏ –æ–Ω —Å–æ–æ–±—â–∞–µ—Ç –æ —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏–∏ ‚Äî –∑–∞–≤–µ—Ä—à–∞–µ–º.
        if (isinstance(message, ToolMessage) and
                "saved" in message.content.lower() and
                "document" in message.content.lower()):
            return "end"

    return "continue"


